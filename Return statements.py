def multiply(number1, number2):
    result = number1 * number2
    return result                 #a função return irá devolver dados para o usuário, no caso, irá "salvar" o resultado de determinada operação

x = multiply(5,8)         #aqui o resultado ficará salvo na variável x

print(x)

