import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))              #round irá arredondar o número
print(math.ceil(pi))          #math.ceil irá arredondar o número para cima
print(math.floor(pi))         #math.floor irá arredondar o número para baixo
print(abs(pi))                #abs irá mostrar o módulo do número (ou seja se o número for negativo, irá mostrar em positivo)
print(pow(pi,2))              #pow (power) irá elevar um número a determinada potencia: (base , expoente)
print(math.sqrt(z))           #math.sqrt (square root) irá tirar a raiz quadrada de um número
print(max(x,y,z))             #max irá mostrar o maior número da seleção
print(min(x,y,z))             #min irá mostrar o menor número da seleção