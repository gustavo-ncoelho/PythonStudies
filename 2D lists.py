#2D lists = a lists of lists

drinks = ["coffee","soda","tea"]
dinner = ["pizza","hamburger","hotdog"]
dessert = ["cake","ice cream"]

food = [drinks,dinner,dessert]

print(food[0][1])          #Nesse caso, o primeiro [] pega o endereço da variável foods e o segundo [] pega o endereço da variável drinks
print(food[1][1])
print(food[2][0])




